func UnitsByRace(race data.Race) []Unit {
    var units []Unit

    for _, unit := range AllUnits {
        if unit.Race == race {
            units = append(units, unit)
        }
    }

    return units
}

func UnitsByRealm(realm data.MagicType) []Unit {
    var units []Unit

    for _, unit := range AllUnits {
        if unit.Realm == realm {
            units = append(units, unit)
        }
    }

    return units
}

func ChooseRandomUnit(race data.Race) Unit {
    var units []Unit
    for _, unit := range AllUnits {
        if unit.Race == race && unit.Name != "Settlers" {
            units = append(units, unit)
        }
    }

    return units[rand.N(len(units))]
}

func GetUnitByName(name string) Unit {
    for _, unit := range AllUnits {
        if unit.Name == name {
            return unit
        }
    }

    return UnitNone
}
